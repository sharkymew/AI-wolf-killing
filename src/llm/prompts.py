class PromptManager:
    @staticmethod
    def get_system_prompt(role: RoleType, player_id: int, personality: str = "") -> str:
        base = f"你是玩家 {player_id}。\n"
        if personality:
            desc = PERSONALITIES.get(personality, "")
            base += f"\n【你的性格：{personality}】\n{desc}\n"
        role_specific = ROLE_PROMPTS.get(role, "")
        return base + COMMON_INSTRUCTIONS + role_specific
